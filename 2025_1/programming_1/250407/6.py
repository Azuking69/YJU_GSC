# 비교 연산자 : 좌항과 우항의 값을 비교
# 비교 연산자 종류 : >, >=, <, <=, ==, !=

print(3 > 4)            # false
print(2 >= 2)           # false
print(2.0 >= 2)         # true
print(3 < 4)            # true
print(4 <= 4)           # true
print(1 == 1)           # true
print(1 != 2)           # true
print(True != False)    # true

print("a" > "c")        # false
