package Java_drill.drill_001;

public class main1_21 {
    public static void main(String[] args) {
        // 변수선어
        int a = 5;
        int b = 10;
        // 확인
        System.out.println(a);
        System.out.println(b);
        // 값 교환
        int temp = a;
        a = b;
        b = temp;
        // 출력
        System.out.println(a);
        System.out.println(b);
    }
}
