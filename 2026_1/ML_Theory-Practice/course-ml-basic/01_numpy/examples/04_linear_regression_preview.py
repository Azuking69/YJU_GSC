"""
NumPy 활용 - 선형회귀 맛보기
=============================
for문으로 작성한 선형회귀 코드를 numpy로 변환하면 어떻게 바뀌는지 비교한다.
"numpy를 왜 배우는가?"에 대한 답을 직접 확인한다.
"""

import numpy as np

# ============================================================
# 데이터셋: H(x) = 0.5x + 2  →  w=0.5, b=2
# ============================================================
x_data = [1, 2, 3, 4]
y_data = [2.5, 3.0, 3.5, 4.0]

# numpy 배열로도 준비
x_np = np.array(x_data, dtype=np.float64)
y_np = np.array(y_data, dtype=np.float64)


# ============================================================
# 방법 1: for문 버전 (02장에서 배운 방식)
# ============================================================
print("=" * 50)
print("방법 1: for문 버전")
print("=" * 50)

w, b = 0.0, 0.0
learning_rate = 0.01
n = len(x_data)

for epoch in range(2000):
    w_grad = 0.0
    b_grad = 0.0
    loss = 0.0

    for x, y in zip(x_data, y_data):
        # 가설: H(x) = w * x + b
        predict = w * x + b

        # 오차: error = H(x) - y
        error = predict - y

        # 기울기 누적
        w_grad += 2 * x * error
        b_grad += 2 * error

        # 손실 누적
        loss += error**2

    # 평균
    w_grad /= n
    b_grad /= n
    loss /= n

    # 업데이트
    w = w - learning_rate * w_grad
    b = b - learning_rate * b_grad

print(f"w = {w:.4f}, b = {b:.4f}")


# ============================================================
# 방법 2: numpy 버전
#   - for문 없이 배열 연산만으로 동일한 결과를 얻는다.
#   - 코드가 짧고, 대규모 데이터에서 훨씬 빠르다.
# ============================================================
print(f"\n{'=' * 50}")
print("방법 2: numpy 버전")
print("=" * 50)

w, b = 0.0, 0.0

for epoch in range(2000):
    # 가설: for문 4줄 → numpy 1줄
    y_pred = w * x_np + b

    # 손실: for문 4줄 → numpy 1줄
    loss = np.mean((y_pred - y_np) ** 2)

    # 기울기: for문 6줄 → numpy 2줄
    error = y_pred - y_np
    grad_w = (2 / n) * np.sum(error * x_np)
    grad_b = (2 / n) * np.sum(error)

    # 업데이트 (동일)
    w = w - learning_rate * grad_w
    b = b - learning_rate * grad_b

print(f"w = {w:.4f}, b = {b:.4f}")


# ============================================================
# 비교 정리
# ============================================================
